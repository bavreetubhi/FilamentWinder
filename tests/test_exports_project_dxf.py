def test_profile_helical_path_uses_local_radius_and_surface_slope() -> None:
    profile = AxisymmetricProfileMandrel(
        z_mm=np.asarray([0.0, 50.0, 100.0]),
        r_mm=np.asarray([20.0, 30.0, 20.0]),
    )
    config = ProfileHelicalPathConfig(
        winding_angle_deg=35.0,
        tow_width_mm=3.0,
        point_count=25,
        min_radius_mm=10.0,
    )

    path = ProfileHelicalPathGenerator(profile, config).generate()

    assert path.point_count == 25
    assert path.z_mm[0] == pytest.approx(0.0)
    assert path.z_mm[-1] == pytest.approx(100.0)
    assert path.theta_rad[-1] > 0.0
