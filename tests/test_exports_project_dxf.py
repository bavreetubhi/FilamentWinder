def test_ascii_dxf_lwpolyline_imports_zr_profile(tmp_path: Path) -> None:
    dxf_path = tmp_path / "profile.dxf"
    dxf_tags: list[Any] = [
        "0",
        "SECTION",
        "2",
        "ENTITIES",
        "0",
        "LWPOLYLINE",
        "90",
        "3",
        "10",
        "0",
        "20",
        "0",
        "10",
        "50",
        "20",
        "20",
        "10",
        "100",
        "20",
        "10",
        "0",
        "ENDSEC",
        "0",
        "EOF",
    ]
    dxf_path.write_text("\n".join(str(tag) for tag in dxf_tags), encoding="utf-8")

    profile = import_dxf_zr_profile(dxf_path)

    assert profile.z_mm.tolist() == [0.0, 50.0, 100.0]
    assert profile.r_mm.tolist() == [0.0, 20.0, 10.0]
    assert profile.radius_at([75.0])[0] == pytest.approx(15.0)


