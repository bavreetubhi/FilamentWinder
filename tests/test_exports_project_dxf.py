def _generate_program() -> tuple[CylinderMandrel, SurfacePath, MachineMotionTable]:
    mandrel = CylinderMandrel(length_mm=100.0, radius_mm=20.0)
    config = HelicalPathConfig(winding_angle_deg=45.0, tow_width_mm=3.0, point_count=5)
    path = HelicalPathGenerator(mandrel, config).generate()
    motion = machine_path_from_surface_path(path, radial_clearance_mm=5.0)
    return mandrel, path, motion


def test_gcode_export_uses_grbl_style_four_axis_moves(tmp_path: Path) -> None:
    _, _, motion = _generate_program()

    output_path = export_gcode(
        motion,
        tmp_path / "path.gcode",
        options=GCodeOptions(feedrate_mm_min=750.0),
    )

    lines = output_path.read_text(encoding="utf-8").splitlines()
    assert lines[:4] == ["; Generated by FilamentWinder", "; Units: mm and degrees", "G21", "G90"]
    assert lines[4] == "G1 Z0.0000 A0.0000 X25.0000 B45.0000 F750.000"
    assert lines[-1] == "M2"


def test_obj_preview_export_includes_mandrel_path_and_tow_band(tmp_path: Path) -> None:
    mandrel, path, _ = _generate_program()
    tow_band = generate_cylinder_tow_band(mandrel, path)

    output_path = export_cylinder_preview_obj(
        mandrel,
        path,
        tmp_path / "preview.obj",
        tow_band=tow_band,
        theta_segments=8,
        z_segments=2,
    )

    text = output_path.read_text(encoding="utf-8")
    assert "o mandrel" in text
    assert "o helical_path" in text
    assert "o tow_band" in text
    assert "\nl " in text


def test_project_file_round_trip(tmp_path: Path) -> None:
    project = WindingProject(
        name="Test winding",
        mandrel=CylinderMandrelConfig(length_mm=1000.0, radius_mm=100.0),
        winding=WindingConfig(tow_width_mm=6.0, winding_angle_deg=45.0, point_count=500),
        machine=MachineConfig(radial_clearance_mm=25.0, feedrate_mm_min=500.0),
        outputs=OutputConfig(
            csv_path="exports/path.csv",
            gcode_path="exports/path.gcode",
            coverage_csv_path="exports/coverage.csv",
            preview_obj_path="exports/path.obj",
        ),
    )

    output_path = save_project(project, tmp_path / "project.fwp.json")
    loaded = load_project(output_path)

    assert loaded == project


